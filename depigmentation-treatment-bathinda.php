          <h2 class="section-title">Understanding Pigmentation: What Causes It?</h2>
          <p>Skin pigmentation occurs when melanin (the pigment responsible for skin color) is overproduced or unevenly distributed. Common triggers include:</p>
          <ul>
            <li><strong>Sun Exposure:</strong> UV rays stimulate melanocytes, leading to sunspots and freckles.</li>
            <li><strong>Hormonal Changes:</strong> Melasma (gray-brown patches) often arises during pregnancy or due to birth control.</li>
            <li><strong>Acne or Inflammation:</strong> Post-inflammatory hyperpigmentation (PIH) lingers after breakouts heal.</li>
            <li><strong>Aging:</strong> Cumulative sun damage and slower cell turnover cause age spots.</li>
            <li><strong>Genetics:</strong> Some skin types are prone to pigmentation disorders.</li>
          </ul>
          <p>Our dermatologists begin with a thorough analysis of your skin type, lifestyle, and pigmentation cause to design a targeted treatment plan.</p>
