          <h2 class="section-title">Understanding Scars: Types & Causes</h2>
          <p>Scars form when the skin repairs itself after trauma. Their appearance depends on factors like injury depth, genetics, and aftercare. Common types include:</p>
          <ul>
            <li><strong>Hypertrophic Scars:</strong> Raised, red scars that stay within the injury site.</li>
            <li><strong>Keloids:</strong> Overgrown scars that spread beyond the original wound (common in darker skin tones).</li>
            <li><strong>Atrophic Scars:</strong> Depressed, pitted scars (e.g., acne or chickenpox scars).</li>
            <li><strong>Contracture Scars:</strong> Tight, shiny scars from burns that may limit movement.</li>
            <li><strong>Stretch Marks:</strong> Streaks caused by rapid skin stretching (pregnancy, growth spurts).</li>
          </ul>
